# Area of Circle
radius = float(input("Enter Radius: "))
area = 3.14 * radius * radius
print("Area =", area)

# Average of 3 Subjects
m1 = float(input("Enter Marks 1: "))
m2 = float(input("Enter Marks 2: "))
m3 = float(input("Enter Marks 3: "))

avg = (m1 + m2 + m3) / 3
print("Average =", avg)

